// This library include basic math operations
class math
{
	public :
		// returns the  addition of arguments
		int add(int, int);
		
		// returns the subtraction of arguments
		int sub(int, int);
		
		// returns the multiplication of arguments
		int mul(int, int);
		
		// returns the floor division of arguments
		int div(int, int);
		
		// returns the value of first argument raise to second 
		long long pow(int, int);
		
};
